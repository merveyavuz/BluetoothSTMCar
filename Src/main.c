/**
  * @brief  The application entry point.
  *
  * @retval None
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_TIM1_Init();
  MX_TIM3_Init();
  MX_USART2_UART_Init();
  MX_USART3_UART_Init();
	

  /* Initialize interrupts */
  MX_NVIC_Init();
  /* USER CODE BEGIN 2 */
	HAL_TIM_PWM_Start(&htim1,TIM_CHANNEL_4);
	
	HAL_TIM_PWM_Start(&htim3,TIM_CHANNEL_1);
	HAL_TIM_PWM_Start(&htim3,TIM_CHANNEL_2);
	HAL_TIM_PWM_Start(&htim3,TIM_CHANNEL_3);
	HAL_TIM_PWM_Start(&htim3,TIM_CHANNEL_4);
	
	HAL_UART_Receive_IT(&huart2, Rx_data, 1); 

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {

  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */
		//HAL_GPIO_TogglePin(LD6_GPIO_Port,LD6_Pin);		
			HAL_Delay(60);
		
			if(Transfer_cplt){		
			buffer[0]='O';
			buffer[1]='K';
			HAL_UART_Transmit(&huart2,buffer,2,1000);							


			//GRIPPER 0 Derece
			if(Rx_Buffer[0]=='G' && Rx_Buffer[1]=='R' && Rx_Buffer[2]=='P' && Rx_Buffer[3]=='0')
			{	
				duty=145;
				__HAL_TIM_SET_COMPARE(&htim1,TIM_CHANNEL_4,duty);
			}
			
			//GRIPPER +90 Derece
			if(Rx_Buffer[0]=='G' && Rx_Buffer[1]=='R' && Rx_Buffer[2]=='P' && Rx_Buffer[3]=='+' && Rx_Buffer[4]=='9' && Rx_Buffer[5]=='0')
			{	
				duty=255;
				__HAL_TIM_SET_COMPARE(&htim1,TIM_CHANNEL_4,duty);
			}
			
			//GRIPPER -90 Derece
			if(Rx_Buffer[0]=='G' && Rx_Buffer[1]=='R' && Rx_Buffer[2]=='P' && Rx_Buffer[3]=='-' && Rx_Buffer[4]=='9' && Rx_Buffer[5]=='0')
			{	
				duty=55;
				__HAL_TIM_SET_COMPARE(&htim1,TIM_CHANNEL_4,duty);
			}
			
			//GRIPPER Her Derece
			if(Rx_Buffer[0]=='G' && Rx_Buffer[1]=='R' && Rx_Buffer[2]=='P' && Rx_Buffer[3]=='A' && Rx_Buffer[4]=='N' && Rx_Buffer[5]=='Y')
			{	
				duty=0;
				if (Rx_Buffer[6] >= '0' && Rx_Buffer[6] <= '9'){					
					digit = Rx_Buffer[6] - '0';
					duty=duty+(digit*100);
				}
				if (Rx_Buffer[7] >= '0' && Rx_Buffer[7] <= '9'){					
					digit = Rx_Buffer[7] - '0';
					duty=duty+(digit*10);
				}
				if (Rx_Buffer[8] >= '0' && Rx_Buffer[8] <= '9'){					
					digit = Rx_Buffer[8] - '0';
					duty=duty+(digit*1);
				}				
				__HAL_TIM_SET_COMPARE(&htim1,TIM_CHANNEL_4,duty);
			}

			//LEFT
			if(Rx_Buffer[0]=='L' && Rx_Buffer[1]=='E' && Rx_Buffer[2]=='F' && Rx_Buffer[3]=='T')
			{	
				for(pwm=0;pwm<Max_Speed;pwm++){
					__HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_1,0);
					__HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_2,pwm);					
					HAL_Delay(1);
				}
			}
			
			//RIGHT
			if(Rx_Buffer[0]=='R' && Rx_Buffer[1]=='I' && Rx_Buffer[2]=='G' && Rx_Buffer[3]=='H' && Rx_Buffer[4]=='T')
			{	
				for(pwm=0;pwm<Max_Speed;pwm++){
					__HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_3,0);
					__HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_4,pwm);					
					HAL_Delay(1);
				}
			}	
			
			//FORWARD
			if(Rx_Buffer[0]=='F' && Rx_Buffer[1]=='R' && Rx_Buffer[2]=='W')
			{	
				for(pwm=0;pwm<Max_Speed;pwm++){
					__HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_1,0);
					__HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_2,pwm);	
					__HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_3,0);
					__HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_4,pwm);					
					HAL_Delay(1);
				}
			}
			
			//BACK
			if(Rx_Buffer[0]=='B' && Rx_Buffer[1]=='A' && Rx_Buffer[2]=='C' && Rx_Buffer[3]=='K')
			{	
				for(pwm=0;pwm<Max_Speed;pwm++){
					__HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_1,pwm);
					__HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_2,0);	
					__HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_3,pwm);
					__HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_4,0);					
					HAL_Delay(1);
				}
			}			
			
			//RETURN LEFT
			if(Rx_Buffer[0]=='R' && Rx_Buffer[1]=='T' && Rx_Buffer[2]=='L')
			{	
				for(pwm=0;pwm<Max_Speed_RL;pwm++){
					__HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_1,0);
					__HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_2,pwm);	
					__HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_3,pwm);
					__HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_4,0);					
					HAL_Delay(1);
				}
			}			
			
			//RETURN RIGHT
			if(Rx_Buffer[0]=='R' && Rx_Buffer[1]=='T' && Rx_Buffer[2]=='R')
			{	
				for(pwm=0;pwm<Max_Speed_RL;pwm++){
					__HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_1,pwm);
					__HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_2,0);	
					__HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_3,0);
					__HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_4,pwm);					
					HAL_Delay(1);
				}
			}			
			
			//STOP
			if(Rx_Buffer[0]=='S' && Rx_Buffer[1]=='T' && Rx_Buffer[2]=='O' && Rx_Buffer[3]=='P')
			{					
					__HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_1,0);
					__HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_2,0);	
					__HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_3,0);
					__HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_4,0);				
			}
			Transfer_cplt=0;
		}

  }
  /* USER CODE END 3 */

}

